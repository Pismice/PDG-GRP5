  FirebaseAuth.instance.authStateChanges().listen((User? u) {
    if (u == null) {
      //print('User is currently signed out!');
    } else {
      //print('User is signed in!');
    }
  });

  // Lancer l'app
  //final db = FirebaseFirestore.instance;
  runApp(const MyApp());
